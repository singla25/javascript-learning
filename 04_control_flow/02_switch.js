const month = 3
switch (month) {
    case 1:
        console.log("January")
        break
    case 2:
        console.log("February")
        break
    case 3:
        console.log("March")
        break
    case 4:
        console.log("April")
        break
    case 5:
        console.log("May")
        break
    case 6:
        console.log("June")
        break
    default:
        console.log("Default case match")  
}
console.log()


let fruit = "apple";
switch (fruit) {
  case "apple":
    console.log("It’s an apple");
    break;
  case "banana":
    console.log("It’s a banana");
    break;
  default:
    console.log("Unknown fruit");
}
console.log()


